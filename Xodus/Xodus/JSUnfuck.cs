        public string decode(bool replace_plus = true)
        {
            var start_js = "";

            while (true)
            {
                start_js = js;
                replace_words();
                replace_numbers();
                replace_arrays();
                replace_uniqs();

                if (start_js == js)
                    break;
            }

            if (replace_plus)
                js = js.Replace("+", "");

            js = Regex.Replace(js, @"\[[A-Za-z]*\]", "");
            js = Regex.Replace(js, @"\[(\d+)\]", @"\\1;");
            return js;
        }

        private void replace_words()
        {
            foreach (var item in words)
                js = js.Replace(item.Key, item.Value);
        }

        private void replace_numbers()
        {
            foreach (var item in numbers)
                js = js.Replace(item.Key, item.Value);
        }

        private void replace_arrays()
        {
            foreach (var word in words.Values)
                for (var i = 0; i < 100; i++)
                    try
                    {
                        var repl = word[i];
                        js = js.Replace(word + "[" + i + "]", repl.ToString());
                    }
                    catch (Exception)
                    {
                    }
        }

        private void replace_uniqs()
        {
            foreach (var item in uniqs)
                if (js.Contains(item.Key))
                    if (item.Value == 1)
                        handleToString();
                    else if (item.Value == 2)
                        handleEscape(item.Key);
                    else if (item.Value == 3)
                        handleUnescape(item.Key);
        }

        private void handleUnescape(string key)
        {
            var start = 0;

            while (true)
            {
                var offset = js.IndexOf(key, start);

                if (offset == -1)
                    return;

                offset += key.Length;
                var expr = char.MinValue;
                var extra = char.MinValue;
                var last_c = js[offset - 1];
                var abort = false;

                var shit = js.Substring(offset);

                var i = 0;
                foreach (var c in shit)
                {
                    extra += c;
                    if (c == ')')
                        break;
                    if (i > 0 && c == '(' || c == '[' && last_c != '+')
                    {
                        abort = true;
                        break;
                    }
                    if (c == '%' || hexdigits.Contains(c))
                        expr += c;

                    last_c = c;
                }

                if (!abort)
                {
                    js = js.Replace(key + extra, Uri.UnescapeDataString(expr.ToString()));
                    break;
                }
                start = offset;
            }
        }

        private void handleEscape(string key)
        {
            var offset = js.IndexOf(key) + key.Length;

            if (js[offset] == '(' && js[offset + 2] == ')')
            {
                var c = js[offset + 1];
                js = js.Replace(key + "(" + c + ")", Uri.EscapeDataString(c.ToString()));
            }
        }

        private void handleToString()
        {
            foreach (Match match in Regex.Matches(@"(\d+)\[t\+o\+S\+t\+r\+i\+n\+g\](\d+)", js))
            {
                var repl = tobase(match.Groups[1].Value, match.Groups[2].Value);
                js = js.Replace(match.Groups[0].Value, repl);
            }
        }


        private string tobase(string num, string basenum)
        {
            int n;
            int b;

            if (!int.TryParse(num, out n))
                return "";

            if (!int.TryParse(basenum, out b))
                return "";

            var result = string.Empty;
            do
            {
                result = "0123456789ABCDEF"[n % b] + result;
                n /= b;
            } while (n > 0);

            return result;
        }
